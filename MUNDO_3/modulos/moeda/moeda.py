# modulos

def aumentar(numero: int, porcentagem: int):
    return numero + (numero * (porcentagem / 100))


def diminuir(numero: int, porcentagem: int):
    return numero - (numero * (porcentagem / 100))


def dobro(numero: int):
    return numero * 2


def metade(numero: int):
    return numero / 2
