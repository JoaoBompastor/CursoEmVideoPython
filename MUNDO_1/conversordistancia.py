#conversor de distancia!

numero = float(input('digite uma distancia em metros: '))

print('{} km' .format(numero/1000))
print('{} hm' .format(numero/100))
print('{} dam' .format(numero/10))
print('{} m' .format(numero))
print('{} dm' .format(numero*10))
print('{} cm' .format(numero*100))
print('{} mm' .format(numero*1000))
